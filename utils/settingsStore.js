export async function getFavoriteAppIds() {
  if (typeof window === 'undefined') return [...DEFAULT_SETTINGS.favoriteIds];
  return readIds(FAVORITES_KEY, DEFAULT_SETTINGS.favoriteIds);
}

export async function setFavoriteAppIds(ids) {
  writeIds(FAVORITES_KEY, Array.isArray(ids) ? ids : []);
}

export async function getPinnedAppIds() {
  if (typeof window === 'undefined') return [...DEFAULT_SETTINGS.pinnedIds];
  return readIds(PINNED_KEY, DEFAULT_SETTINGS.pinnedIds);
}

export async function setPinnedAppIds(ids) {
  writeIds(PINNED_KEY, Array.isArray(ids) ? ids : []);
}

export async function resetSettings() {
  if (typeof window === 'undefined') return;
  await Promise.all([
    del('accent'),
    del('bg-image'),
  ]);
  window.localStorage.removeItem('density');
  window.localStorage.removeItem('reduced-motion');
  window.localStorage.removeItem('font-scale');
  window.localStorage.removeItem('high-contrast');
  window.localStorage.removeItem('large-hit-areas');
  window.localStorage.removeItem('pong-spin');
  window.localStorage.removeItem('allow-network');
  window.localStorage.removeItem('haptics');
  window.localStorage.removeItem('use-kali-wallpaper');
  window.localStorage.removeItem(FAVORITES_KEY);
  window.localStorage.removeItem(PINNED_KEY);
}

export async function exportSettings() {
  const [
    accent,
    wallpaper,
    useKaliWallpaper,
    density,
    reducedMotion,
    fontScale,
    highContrast,
    largeHitAreas,
    pongSpin,
    allowNetwork,
    haptics,
    favoriteIds,
    pinnedIds,
  ] = await Promise.all([
    getAccent(),
    getWallpaper(),
    getUseKaliWallpaper(),
    getDensity(),
    getReducedMotion(),
    getFontScale(),
    getHighContrast(),
    getLargeHitAreas(),
    getPongSpin(),
    getAllowNetwork(),
    getHaptics(),
    getFavoriteAppIds(),
    getPinnedAppIds(),
  ]);
  const theme = getTheme();
  return JSON.stringify({
    accent,
    wallpaper,
    density,
    reducedMotion,
    fontScale,
    highContrast,
    largeHitAreas,
    pongSpin,
    allowNetwork,
    haptics,
    useKaliWallpaper,
    theme,
    favoriteIds,
    pinnedIds,
  });
}

export async function importSettings(json) {
  if (typeof window === 'undefined') return;
  let settings;
  try {
    settings = typeof json === 'string' ? JSON.parse(json) : json;
  } catch (e) {
    console.error('Invalid settings', e);
    return;
  }
  const {
    accent,
    wallpaper,
    useKaliWallpaper,
    density,
    reducedMotion,
    fontScale,
    highContrast,
    largeHitAreas,
    pongSpin,
    allowNetwork,
    haptics,
    theme,
    favoriteIds,
    pinnedIds,
  } = settings;
  if (accent !== undefined) await setAccent(accent);
  if (wallpaper !== undefined) await setWallpaper(wallpaper);
  if (useKaliWallpaper !== undefined) await setUseKaliWallpaper(useKaliWallpaper);
  if (density !== undefined) await setDensity(density);
  if (reducedMotion !== undefined) await setReducedMotion(reducedMotion);
  if (fontScale !== undefined) await setFontScale(fontScale);
  if (highContrast !== undefined) await setHighContrast(highContrast);
  if (largeHitAreas !== undefined) await setLargeHitAreas(largeHitAreas);
  if (pongSpin !== undefined) await setPongSpin(pongSpin);
  if (allowNetwork !== undefined) await setAllowNetwork(allowNetwork);
  if (haptics !== undefined) await setHaptics(haptics);
  if (theme !== undefined) setTheme(theme);
  if (favoriteIds !== undefined) await setFavoriteAppIds(favoriteIds);
  if (pinnedIds !== undefined) await setPinnedAppIds(pinnedIds);
}

export const defaults = DEFAULT_SETTINGS;
